// Función para obtener traducción del coreano
function getPokemonNameTranslationKO(koreanName) {
  return pokemonNameTranslationsKO[koreanName] || null;
}

// Función para formatear nombre con traducción
function formatPokemonNameKO(pokemonName) {
  const translation = getPokemonNameTranslationKO(pokemonName);
  if (translation && pokemonName !== translation) {
    return `${pokemonName} (${translation})`;
  }
  return pokemonName;
}

