// Función para obtener traducción del chino
function getPokemonNameTranslationZH(chineseName) {
  return pokemonNameTranslationsZH[chineseName] || null;
}

// Función para formatear nombre con traducción
function formatPokemonNameZH(pokemonName) {
  const translation = getPokemonNameTranslationZH(pokemonName);
  if (translation && pokemonName !== translation) {
    return `${pokemonName} (${translation})`;
  }
  return pokemonName;
}

